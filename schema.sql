-- ---------------------------------------------------------
-- Tabla: alumno
-- Contiene la información personal y académica básica de los alumnos inscritos en la institución.
-- ---------------------------------------------------------

CREATE TABLE alumno (
  numero_cuenta      INT PRIMARY KEY,				-- Número único del alumno
  nombre             VARCHAR(25) NOT NULL,			-- Nombre(s)
  apellido_paterno   VARCHAR(25) NOT NULL,			-- Apellido paterno
  apellido_materno   VARCHAR(25) NOT NULL,			-- Apellido materno
  curp               CHAR(18)    NOT NULL UNIQUE,	-- CURP del alumno
  telefono           CHAR(10)    NOT NULL,			-- Teléfono de contacto
  sexo               CHAR(1)     NOT NULL,			-- Sexo (M/F)
  correo_electronico VARCHAR(100) NOT NULL UNIQUE,	-- Correo institucional/personal
  fecha_nacimiento   DATE        NOT NULL,			-- Fecha de nacimiento
  foto_perfil        BYTEA NULL,                    -- Fotografía del alumno, imagen/binario (punto 7)
  id_entidad         INT         NOT NULL,			-- Entidad federativa de origen

-- Restricciones de validación
  CONSTRAINT ck_alumno_sexo CHECK (sexo IN ('M','F')),
  CONSTRAINT ck_alumno_tel  CHECK (telefono ~ '^[0-9]{10}$'),

-- Relación con entidad_federativa
  CONSTRAINT fk_alumno_entidad
    FOREIGN KEY (id_entidad)
    REFERENCES entidad_federativa(id_entidad)
    ON DELETE RESTRICT
    ON UPDATE CASCADE
);


