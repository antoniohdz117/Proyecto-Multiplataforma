CREATE TABLE historial (
  id_historial INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,	-- Identificador del historial
  folio_acta VARCHAR(10) NOT NULL UNIQUE,							-- Folio del acta
  id_alumno_curso INT NOT NULL,										-- Inscripción evaluada
  id_credito INT NOT NULL,											-- Crédito asociado
  clave_plantel INT NOT NULL,										-- Plantel correspondiente
  promedio_final NUMERIC(4,2) NULL,									-- Promedio final

-- Un historial por inscripción
  CONSTRAINT uq_hist_ac UNIQUE (id_alumno_curso),

  CONSTRAINT fk_hist_ac FOREIGN KEY (id_alumno_curso)
    REFERENCES alumno_curso(id_alumno_curso)
    ON DELETE RESTRICT ON UPDATE CASCADE,

  CONSTRAINT fk_hist_credito FOREIGN KEY (id_credito)
    REFERENCES credito(id_credito)
    ON DELETE RESTRICT ON UPDATE CASCADE,

  CONSTRAINT fk_hist_plantel FOREIGN KEY (clave_plantel)
    REFERENCES plantel(clave_plantel)
    ON DELETE RESTRICT ON UPDATE CASCADE
);


