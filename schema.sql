-- ---------------------------------------------------------
-- Tabla: historial
-- Guarda el registro "oficial" de la calificación final de una inscripción
-- (lo que sería el historial académico / acta de calificaciones).
--
-- Campo: folio_acta
--   - Es un identificador alfanumérico del acta donde quedó registrada
--     la calificación del alumno en ese curso.
--   - Se genera automáticamente en los triggers con el formato
--     'AC' + id_alumno_curso con ceros a la izquierda (ej. 'AC00000017').
--   - Es UNIQUE, para que cada acta tenga un folio distinto y sea fácil
--     de localizar o referenciar en reportes.
