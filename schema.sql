-- ---------------------------------------------------------
-- Tabla: asignacion_curso 
-- es una tabla intermedia que relaciona
--       curso + horario + salón.
--     Cada fila significa: "el curso X se imparte en el salón Y en el horario Z".
--
-- Con la restricción UNIQUE (id_horario, id_salon) en `asignacion_curso`
-- evitamos que haya dos cursos al mismo tiempo en el mismo salón.
-- Además, permitimos que:
--   - un mismo curso pueda tener varios horarios/salones (varias filas),
--   - y un mismo salón se use en diferentes horarios sin empalmes.
-- ---------------------------------------------------------

CREATE TABLE asignacion_curso (
  id_asignacion_curso INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,	-- Identificador de la asignación
  id_horario INT NOT NULL,												-- Horario asignado
  id_curso   INT NOT NULL,												-- Curso asignado
  id_salon   INT NOT NULL,												-- Salón asignado

-- Relaciones
  CONSTRAINT fk_asig_horario FOREIGN KEY (id_horario)
    REFERENCES horario(id_horario)
    ON DELETE RESTRICT ON UPDATE CASCADE,

  CONSTRAINT fk_asig_curso FOREIGN KEY (id_curso)
    REFERENCES curso(id_curso)
    ON DELETE RESTRICT ON UPDATE CASCADE,

  CONSTRAINT fk_asig_salon FOREIGN KEY (id_salon)
    REFERENCES salon(id_salon)
    ON DELETE RESTRICT ON UPDATE CASCADE,

-- Evita que un salón tenga dos cursos al mismo horario
  CONSTRAINT uq_asig_horario_salon UNIQUE (id_horario, id_salon)
);

-- ---------------------------------------------------------
-- Tabla: alumno_curso
-- Registra la inscripción de alumnos a cursos y sus evaluaciones parciales.
--
-- Campo: estatus_evaluacion
--   - Indica en qué situación quedó la evaluación del alumno en ese curso.
--   - Usamos dos valores:
--       'REG' → Alumno regular (cursó normalmente).
--       'NP'  → No presentó / no terminó la materia.
--   - Por defecto es 'REG'.
--   - Cuando es 'NP', los triggers se encargan de limpiar las calificaciones
--     parciales y evitar que se calcule un promedio final.
-- ---------------------------------------------------------

CREATE TABLE alumno_curso (
  id_alumno_curso INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,	-- Identificador de la inscripción
  primer_parcial  NUMERIC(4,2) NULL,								-- Calificación primer parcial
  segundo_parcial NUMERIC(4,2) NULL,								-- Calificación segundo parcial
  tercer_parcial  NUMERIC(4,2) NULL,								-- Calificación tercer parcial
  estatus_evaluacion VARCHAR(3) NOT NULL DEFAULT 'REG',				-- Estatus de evaluación
  numero_cuenta   INT NOT NULL,										-- Alumno inscrito
  id_curso        INT NOT NULL,										-- Curso inscrito

-- Evita inscripciones duplicadas
  CONSTRAINT uq_alumno_curso UNIQUE (numero_cuenta, id_curso),

-- Validaciones de calificaciones
  CONSTRAINT ck_p1 CHECK (primer_parcial  IS NULL OR (primer_parcial  BETWEEN 0 AND 10)),
  CONSTRAINT ck_p2 CHECK (segundo_parcial IS NULL OR (segundo_parcial BETWEEN 0 AND 10)),
  CONSTRAINT ck_p3 CHECK (tercer_parcial  IS NULL OR (tercer_parcial  BETWEEN 0 AND 10)),
  CONSTRAINT ck_estatus_final CHECK (estatus_evaluacion IN ('REG','NP')),

-- Regla: si es NP no puede haber calificaciones
  CONSTRAINT ck_np_sin_notas CHECK (
    (estatus_evaluacion = 'REG')
    OR
    (estatus_evaluacion = 'NP'
      AND primer_parcial  IS NULL
      AND segundo_parcial IS NULL
      AND tercer_parcial  IS NULL)
  ),

-- Relaciones
  CONSTRAINT fk_ac_alumno FOREIGN KEY (numero_cuenta)
    REFERENCES alumno(numero_cuenta)
    ON DELETE RESTRICT ON UPDATE CASCADE,

  CONSTRAINT fk_ac_curso FOREIGN KEY (id_curso)
    REFERENCES curso(id_curso)
    ON DELETE RESTRICT ON UPDATE CASCADE
);

